const pilotos = ['Vettel', 'Alonso', 'Raikkonen', 'Massa']
pilotos.pop() //Remove o ultimo elemento do array
console.log(pilotos)

pilotos.push('Verstappen') //Adiciona um novo elemento dentro do array na posição final
console.log(pilotos)

pilotos.shift() //Remove o primeiro elemento da array
console.log(pilotos)

pilotos.unshift('Hamilton') //Adiciona um elemento a array na primeira posição
console.log(pilotos)